public class Pebble {

    // attributes
    private int weight;

    /**
     * Constructor for the Pebble class
     * @param weight
     */
    public Pebble(int weight){
        this.weight = weight;
    }

    /**
     * Sets the weight
     * @param weight
     */
    public void setWeight(int weight) {this.weight = weight;}
    /**
     * Gets the weight of the pebble
     * @return weight
     */
    public int getWeight() {return weight;}

}
